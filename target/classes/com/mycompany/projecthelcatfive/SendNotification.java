    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String emailt = emailto.getText(); 
        String fromemail = emailf.getText();
        String fromemailpassword = "Tgbnm123";
        String emailss = emails.getText();
         
        Properties properties = new Properties();
        properties.put("mail.smtp.auth","true");
        properties.put("mail.smtp.starttls.enable","true");
        properties.put("mail.smtp.host","smtp.gmail.com");
        properties.put("mail.smtp.port","587");
        
        Session session = Session.getDefaultInstance(properties, new javax.mail.Authenticator(){
        protected PasswordAuthentication getPasswordAuthentication(){
            return new PasswordAuthentication(fromemail,fromemailpassword);
            
        }
   });
       try {
           MimeMessage message = new MimeMessage(session);
           message.setFrom(new InternetAddress(fromemail));
           message.addRecipient(Message.RecipientType.TO, new InternetAddress(emailt));
           message.setSubject(emailss);
           message.setText(jTextArea1.getText());
           Transport.send(message);
       } catch (Exception r){
           System.out.println(""+r);
       }
    }//GEN-LAST:event_jButton1ActionPerformed

